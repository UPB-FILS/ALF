'use strict';

//Point a)
function multiply_print (a, b)
{
    console.log(a * b);
}
multiply_print(2, 5);

//Point b)
function multiply_return (a, b)
{
    return a * b;
}
console.log(multiply_return(3, 4));

//Point c)
function multiply_var (a, b)
{
    let mul = a * b;
    console.log(mul);
}
multiply_var(6, 7);

